
# 제약 조건 
/*
 * 
 * 	NOT NULL : NULL 금지
 *  UNIQUE : 중복값입력 금지(NULL은가능)
 *  PRIMARY KEY : NOT NULL + UNIQUE
 *  FOREIGN KEY : 다른 테이블 컬럼을 조회함
 *  CHECK : 조건으로 설정된 값만 허용
 *
 * */
 
CREATE TABLE MEMBER(
	NUM INT NOT NULL PRIMARY KEY,			
    NAME VARCHAR(20) NOT NULL,
    SCORE INT
);




CREATE TABLE MEMBER(
	NUM INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(20) NOT NULL
);












